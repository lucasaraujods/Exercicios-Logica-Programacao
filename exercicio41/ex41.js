//Faça um algoritmo para Imprimir a tabuada do número 5, usando while

let numero=0
console.log("Adição")
while(numero<=10){
    console.log(`${numero} + 5 = ${numero+5}`)
    numero++
}

numero =0
console.log("Subtração:")
while(numero<=10){
    console.log(`${numero} - 5 = ${numero-5}`)
    numero++
}

numero=0
console.log("Multiplicação:")
while(numero<=10){
    console.log(`${numero} x 5 = ${numero*5}`)
    numero++
}

numero=0
console.log("Divisão")
while(numero<=10){
    console.log(`${numero} / 5 = ${numero/5}`)
    numero++
}
